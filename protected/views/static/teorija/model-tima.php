<h1>Модель ТИМa</h1>
<p>Модель ТИМа психики отражает структуру психики, обрабатывающую информацию при взаимодействии с окружающим миром.</p>
<p>Модель ТИМ состоит из 8 функций. Нумерация, принятая в нашей школе, следующая:</p>
