<p>Хорошо видит и понимает течение событий. Умеет предсказать результаты деятельности, ход развития ситуации. Строит прогнозы, которые часто сбываются.</p>
<p>Изучает свойства объектов с точки зрения практического применения. Умеет создавать новый способ работы и инструмент. Добивается оптимальной технологии, позволяющей прежде всего сэкономить время.</p>
<h2>Супер-Эго</h2>
<p>Комфорт и удобство понимает на основании усвоенных норм и стереотипов. Не всегда в состоянии создать себе удобную рабочую или домашнюю обстановку. В эмоциональных состояниях ориентируется только на основании собственного опыта. Часто неадекватен в проявлениях эмоций. Оценки его эмоционального состояния со стороны могут быть ему неприятны.</p>
<h2>Супер-Ид</h2>
